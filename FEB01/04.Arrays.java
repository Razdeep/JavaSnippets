/**
 * Java support arrays with maximum 7 dimensions
 * arrays have a field called 'length' whose return type is int
 * Note: this is not a method of array. It is a field
 */
class Solution{
    public static void main(String args[])
    {
        int marks[];        // This is just a reference variable
        // int [] marks;    // This is also possible
        marks = new int[3];
        marks[0] = 2;
        marks[1] = 3;
        marks[2] = 4;
        
    }
}